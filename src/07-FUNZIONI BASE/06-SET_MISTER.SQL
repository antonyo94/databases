--FUNZIONE DI BASE 6
--Permette ad un dirigente di settare un mister per una certa categoria

CREATE OR REPLACE PROCEDURE SET_MISTER(CATEGORIA VARCHAR,ETA NUMBER,N_PATENTINO CHAR) IS

BEGIN 
UPDATE SQUADRA SET N_PATENTINO_SQUADRA=N_PATENTINO
WHERE CATEGORIA_S=CATEGORIA AND ETA_S=ETA;
COMMIT;

EXCEPTION 
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20004,'Squadra non trovata/Mister non valido');
END;
