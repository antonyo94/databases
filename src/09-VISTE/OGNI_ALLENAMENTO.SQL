--Vista che permette di analizzare gli allenamenti cui hanno partecipato gli allievo della scuola calcio
CREATE OR REPLACE VIEW OGNI_ALLENAMENTO AS
SELECT A.N_TESSERINO AS COD,A.COGNOME_A AS COGNOME,A.NOME_A AS NOME ,S.DATA_ALL_SOSTIENE AS DATA,S.RUOLO_SOSTIENE AS RUOLO
FROM ALLIEVO A,SOSTIENE S
WHERE A.N_TESSERINO=S.N_TESSERINO_SOSTIENE
ORDER BY A.COGNOME_A,A.NOME_A