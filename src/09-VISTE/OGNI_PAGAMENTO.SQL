--Vista che individua tutti i pagamenti effettuati dagli allievi
CREATE OR REPLACE VIEW OGNI_PAGAMENTO AS
SELECT A.N_TESSERINO AS COD,A.COGNOME_A AS COGNOME,A.NOME_A AS NOME,P.DATA_P AS DATA,P.IMPORTO_P AS IMPORTO
FROM ALLIEVO A,PAGAMENTO P
WHERE A.N_TESSERINO=P.N_TESSERINO_PAGAMENTO
ORDER BY A.COGNOME_A,A.NOME_A