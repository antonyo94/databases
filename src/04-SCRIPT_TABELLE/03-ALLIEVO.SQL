DROP TABLE ALLIEVO;

--ALLIEVO
CREATE TABLE ALLIEVO (
	N_TESSERINO CHAR(7),
	CF_G_ALLIEVO CHAR(7) NOT NULL,
	NOME_A VARCHAR2(15),
	COGNOME_A VARCHAR2(15),
	DATA_N DATE,
	VIA_A VARCHAR2(10),
	CAP_A CHAR(5),
	CITTA_A VARCHAR2(10),	
	CONSTRAINT ALLIEVO_PK PRIMARY KEY (N_TESSERINO),
	CONSTRAINT ALLIEVO_FK FOREIGN KEY (CF_G_ALLIEVO) REFERENCES GENITORE(CF_G)
);